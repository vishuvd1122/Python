# Program to calculate area of circle when radius of a circle is taken as input from the user.
r = float(input("Enter radius of a circle:"))            # Format to take input from user in float data type
area = 3.14*r**2                                         # Variable "area" will store the calculated area of circle
print("Area of circle with radius", r, "units:", area, "sq. units")  # Format to print the value in a presentable format
