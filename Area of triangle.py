# Program to calculate the area of triangle by its formula
b = float(input("Enter base of a triangle:"))
h = float(input("Enter height of a triangle:"))
area = 0.5*b*h                                    # "area" will store the calculated value of area of triangle
print("Area of triangle with base and heigth of", b, "units and", h, "units respectively:", area, "sq. units")
