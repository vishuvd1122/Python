# Program to calculate the area of rhombus when both the diagonals are taken as input by the user
d1 = float(input("Enter first diagonal of a rhombus:"))
d2 = float(input("Enter second diagonal of a rhombus:"))
area = 0.5*d1*d2                                               # Formula to calculate area of rhombus
print("Area of rhombus with diagonals of", d1, "units and", d2, "units respectively:", area, "sq. units")
